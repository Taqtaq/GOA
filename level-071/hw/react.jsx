// **Vite** არის თანამედროვე ინსტრუმენტი ფრონტენდ განვითარებისათვის. ის სწრაფად запускает პროექტს, აგროვებს ფაილებს და ავტომატურად ანახლებს გვერდს ბრაუზერში კოდის შეცვლისას.

